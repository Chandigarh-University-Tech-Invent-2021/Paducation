<span class="label warning">Challenges</span><br/><p>
1. Sanitary pads are more expensive, and cause land pollution as compared to its cheaper and more eco- friendly alternatives.<br/> 
2. People are not aware of such alternatives in the rural areas due to lack of information .<br/>
3. using sanitary pads still leds to a stigma in people and they often tend to not talk bout their menstrual health .<br/>
4. Biodegradable pads, tampons, menstrual cups are often more expensive in initially .<br/>
5. Channelize market and accessibility for such products aren't available for women especially in rural areas. <br/><br/></p>

<span class="label success">Solutions</span><br/>
<p>
<b><mark>Technical solutions</mark></b> <br/><br/>
1. 75% of girls experience some problems associated with menstruation including delayed, irregular, painful, and heavy menstrual bleeding we aim at making a website that spreads awareness about menstrual hygiene, directs them to well authorized resources that guides them and answers their questions.<br/>
2. These resources consists of websites, brands and established organizations that offer consultation, medication to take in case of complexities, diet to eat and sites where you can buys biodegradable pads.<br/>
3. Make a period cycle calculator that calculates the expected next date of their period.
4. To store their Data Feed regarding their monthly pad usage in their respective city<br/><br/><br/>




<b><mark>Social/economical/natural solutions</mark></b> <br/><br/>
1. Women can be taught to manufacture the biodegradable pads, this will provide them economic freedom.<br/>
2. Our website will educate women on the topic of menstrual health and thus help to better understand the subject. <br/>
3. Studies have shown that one sanitary pad could take from 500 to 800 years to decompose as the plastic used is not bio-degradable, and can lead to health and environmental hazards, our website will promote biodegradable sanitary pads and other environment friendly alternatives that will led to sustainable development.
<br/><br/>
</p><br/><br/>

